package mycommands.com.myloginpart;

class Item{

    String name;
    int image;
    double rating;

    public Item(String name, int image, double rating){
        this.name = name;
        this.image = image;
        this.rating = rating;
    }

    public Item(String name){
        this.name = name;
        this.image = image;
        this.rating = rating;
    }
}
